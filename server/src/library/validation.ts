////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export type ValidationResult = {
  success: true
} | {
  success: false
  message: string
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export type ValidationsResult = {
  valid: true
} | {
  valid: false
  messages: string[]
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export function processValidationResults(results: ValidationResult[]): ValidationsResult {
  const failures: string[] = []
  for (const result of results) {
    if (!result.success) { failures.push(result.message) }
  }

  let result: ValidationsResult
  if (failures.length === 0) {
    result = { valid: true }
    return result
  }
  else {
    result = { valid: false, messages: failures }
    return result
  }
}

